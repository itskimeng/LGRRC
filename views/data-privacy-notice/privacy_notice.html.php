        <h4>DILG IV-A DATA PRIVACY NOTICE</h4>
        <p style="text-indent:20px;">The Department of the Interior and Local Government (DILG) IV-A shall comply with the Republic Act No. 10173, likewise known as the Data Privacy Act of 2012 by protecting and securing custody of personal data submitted through our DILG IV-A Official Website.</p>

        <h5>When do we collect personal information?</h5>
        <p>DILG collects data through the use of Information System or manually accomplish forms / document when data subjects submit comments, complaints, and requests for assistance and/or documents to our agency.</p>

        <h5>What information is collected?</h5>
        <p>DILG collects the following personal information:</p>
        <ul>
            <li>Full Name</li>
            <li>Email</li>
            <li>Contact Number</li>
            <li>Address</li>
        </ul>

        <h5>Why do we collect personal information?</h5>
        <p>Allow us to provide better service and give proper response / feedback to your requests.</p>

        <h5>How we protect your information?</h5>
        <p style="text-indent:20px;">DILG implements security measures by allowing only authorized access to limited personnel on our system containing sensitive information and required to keep every data confidential.</p>

        <p style="text-indent:20px;">Data subjects have the rights under the Data Privacy Act [RULE VIII. RIGHTS OF DATA SUBJECTS Section 34.]:</p>
        <ul>
            <li>Right to be informed.</li>
            <li>Right to object</li>
            <li>Right to Access</li>
            <li>Right to rectification</li>
            <li>Right to Erasure or Blocking</li>
            <li>Right to damages</li>
        </ul>

        <h5>When do we share the information?</h5>
        <p style="text-indent:20px;">We only share your information if required by the law, or if necessary for compliance associated with a non-disclosure agreement to keep and treat all of your information confidential.</p>

